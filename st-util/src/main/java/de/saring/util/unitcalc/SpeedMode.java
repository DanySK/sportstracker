package de.saring.util.unitcalc;

/**
 * Enumeration of supported speed display or measurement modes. Speed means distance per hour (e.g. km/h), pace means
 * minutes per distance (e.g. min/km). The use of pace is very common for running activities, most other sport types are
 * using the speed.
 *
 * @author Stefan Saring
 */
public enum SpeedMode {
    SPEED, PACE
}
